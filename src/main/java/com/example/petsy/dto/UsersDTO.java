package com.example.petsy.dto;

public class UsersDTO {
	private String username;
	private String id;

	public UsersDTO() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

}
